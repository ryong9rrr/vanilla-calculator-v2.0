import postfix from './postfix'

const TEST_CASE = [
  {
    expArr: [
      '(',
      '(',
      '(',
      '100',
      '-',
      '(',
      '200',
      'X',
      '300',
      ')',
      ')',
      '-',
      '500',
      ')',
      '+',
      '(',
      '20',
      '/',
      '2',
      ')',
      ')',
    ],
    result: ['100', '200', '300', 'X', '-', '500', '-', '20', '2', '/', '+'],
  },
  {
    expArr: ['100', '-', '200', 'X', '300', '-', '500', '+', '20', '/', '2'],
    result: ['100', '200', '300', 'X', '-', '500', '-', '20', '2', '/', '+'],
  },
  {
    expArr: ['1', '+', '3', 'X', '8'],
    result: ['1', '3', '8', 'X', '+'],
  },
  {
    expArr: ['2', 'X', '5', '-', '7'],
    result: ['2', '5', 'X', '7', '-'],
  },
  {
    expArr: ['2.1', 'X', '5.3', '-', '7.0'],
    result: ['2.1', '5.3', 'X', '7.0', '-'],
  },
]

test('algorithms test : postfix()', () => {
  TEST_CASE.forEach(({ expArr, result }) => {
    expect(postfix(expArr)).toEqual(result)
  })
})
